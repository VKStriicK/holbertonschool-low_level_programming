#include <stdio.h>

/**
 * main - Main function
 *
 * Return: Always 0 (Success)
 */

int main(void)
{
	int number = 0;
	int letter = 0;
	for (number == 48; number <= 58; number++)
	{
		putchar(number);
	}
	for (letter == 'a'; letter <= 'f'; letter ++)
	{
		putchar(letter);
	}
	putchar('\n');
	return (0);
}
